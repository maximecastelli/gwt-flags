package com.scrontch.flags.server;

public class FlagSymbolAnchor {
	double width;
	double height;
	double centerX;
	double centerY;

	//Constructor
	FlagSymbolAnchor(double width, double height, double centerX, double centerY) {
		this.width = width;
		this.height = height;
		this.centerX = centerX;
		this.centerY = centerY;
	}
}
